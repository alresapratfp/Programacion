public class Imprimir_Chars {

    public static void imprimir(char caracter, int vegades) {
        for (int i = 0; i < vegades; i++) {
            System.out.print(caracter);
        }
        System.out.println();
    }

    public static void main(String[] args) {
        imprimir('*', 5);
        imprimir('#', 3);
        imprimir('A', 4);
    }
}


